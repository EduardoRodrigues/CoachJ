package coachj.enums;

/**
 * Enum that holds the different zones of the basketball court
 *
 * @author Eduardo M. Rodrigues
 * @version 1.0 
 * @date 05/09/2013
 */
public enum CourtZones {
    
    BENCH(0),
    RIGHT_CORNER (1),
    RIGHT_WING(2),
    RIGHT_LOW_POST(3),
    RIGHT_SIDE_LANE(4),
    RIGHT_ELBOW(5),
    PAINT(6),
    TOP_KEY(7),
    TOP_ARC(8),
    LEFT_LOW_POST(9),
    LEFT_SIDE_LANE(10),
    LEFT_ELBOW(11),
    LEFT_CORNER(12),
    LEFT_WING(13),
    OFFENSIVE_HALFCOURT(14),
    DEFENSIVE_HALFCOURT(15),
    OUT_BOUNDS(16);
    private final int courtZone;

    private CourtZones(int courtZone) {
        this.courtZone = courtZone;
    }
        
    public int getCourtZone() {
        return courtZone;
    }
    
    
}
