package coachj.structures;

/**
 * Stores information about plays from a game
 *
 * @author Eduardo M. Rodrigues
 * @version 1.0
 * @date 12/08/2013
 */
public class Play {

    /**
     * Fields
     */
    private short season;
    private short period;
    private String time;
    private int awayScore;
    private int homeScore;
    private String playType;
    private int ballFrom;
    private int ballTo;
    private int activeOffensivePlayerId;
    private int lastActiveOffensivePlayerId;
    private int activeDefensivePlayerId;
    private int lastActiveDefensivePlayerId;
    private int narrationId;
    private String gameDate;
    private int playerInId;
    private int playerOutId;
    private int playScore;
    private int shotClock;
    private String playDescription;
    
    /**
     * Constructor
     */
    public Play() {
    }

    @Override
    public String toString() {
        return "Play{" + "season=" + season + ", period=" + period + ", time=" + time + ", awayScore=" + awayScore + ", homeScore=" + homeScore + ", playType=" + playType + ", ballFrom=" + ballFrom + ", ballTo=" + ballTo + ", activeOffensivePlayerId=" + activeOffensivePlayerId + ", lastActiveOffensivePlayerId=" + lastActiveOffensivePlayerId + ", activeDefensivePlayerId=" + activeDefensivePlayerId + ", lastActiveDefensivePlayerId=" + lastActiveDefensivePlayerId + ", narrationId=" + narrationId + ", gameDate=" + gameDate + ", playerInId=" + playerInId + ", playerOutId=" + playerOutId + ", playScore=" + playScore + ", shotClock=" + shotClock + ", playDescription=" + playDescription + '}';
    }
    
    

    /* getters and setters */
    public short getPeriod() {
        return period;
    }

    public void setPeriod(short period) {
        this.period = period;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public int getAwayScore() {
        return awayScore;
    }

    public void setAwayScore(int awayScore) {
        this.awayScore = awayScore;
    }

    public int getHomeScore() {
        return homeScore;
    }

    public void setHomeScore(int homeScore) {
        this.homeScore = homeScore;
    }

    public String getPlayType() {
        return playType;
    }

    public void setPlayType(String playType) {
        this.playType = playType;
    }

    public short getSeason() {
        return season;
    }

    public void setSeason(short season) {
        this.season = season;
    }

    public int getBallFrom() {
        return ballFrom;
    }

    public void setBallFrom(int ballFrom) {
        this.ballFrom = ballFrom;
    }

    public int getBallTo() {
        return ballTo;
    }

    public void setBallTo(int ballTo) {
        this.ballTo = ballTo;
    }

    public int getActiveOffensivePlayerId() {
        return activeOffensivePlayerId;
    }

    public void setActiveOffensivePlayerId(int activeOffensivePlayerId) {
        this.activeOffensivePlayerId = activeOffensivePlayerId;
    }

    public int getLastActiveOffensivePlayerId() {
        return lastActiveOffensivePlayerId;
    }

    public void setLastActiveOffensivePlayerId(int lastActiveOffensivePlayerId) {
        this.lastActiveOffensivePlayerId = lastActiveOffensivePlayerId;
    }

    public int getActiveDefensivePlayerId() {
        return activeDefensivePlayerId;
    }

    public void setActiveDefensivePlayerId(int activeDefensivePlayerId) {
        this.activeDefensivePlayerId = activeDefensivePlayerId;
    }

    public int getLastActiveDefensivePlayerId() {
        return lastActiveDefensivePlayerId;
    }

    public void setLastActiveDefensivePlayerId(int lastActiveDefensivePlayerId) {
        this.lastActiveDefensivePlayerId = lastActiveDefensivePlayerId;
    }

    public int getNarrationId() {
        return narrationId;
    }

    public void setNarrationId(int narrationId) {
        this.narrationId = narrationId;
    }

    public String getGameDate() {
        return gameDate;
    }

    public void setGameDate(String gameDate) {
        this.gameDate = gameDate;
    }

    public int getPlayerInId() {
        return playerInId;
    }

    public void setPlayerInId(int playerInId) {
        this.playerInId = playerInId;
    }

    public int getPlayerOutId() {
        return playerOutId;
    }

    public void setPlayerOutId(int playerOutId) {
        this.playerOutId = playerOutId;
    }

    public int getPlayScore() {
        return playScore;
    }

    public void setPlayScore(int playScore) {
        this.playScore = playScore;
    }

    public int getShotClock() {
        return shotClock;
    }

    public void setShotClock(int shotClock) {
        this.shotClock = shotClock;
    }

    public String getPlayDescription() {
        return playDescription;
    }

    public void setPlayDescription(String playDescription) {
        this.playDescription = playDescription;
    }
    
    
    
} // end Play
