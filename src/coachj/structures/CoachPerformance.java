package coachj.structures;

/**
 * Stores information about the performance of a coach in a given
 * franchise
 *
 * @author Eduardo M. Rodrigues
 * @version 1.0
 * @date 14/08/2013
 */
public class CoachPerformance {

    /**
     * Fields
     */
    private short coachId;
    private short franchiseId;
    private short yearsWithFranchise;
    private double performanceRate;

    public CoachPerformance(short coachId, short franchiseId) {
        this.coachId = coachId;
        this.franchiseId = franchiseId;
    }
    
    /**
     * Getters and setters
     */
    public short getCoachId() {
        return coachId;
    }

    public void setCoachId(short coachId) {
        this.coachId = coachId;
    }

    public short getFranchiseId() {
        return franchiseId;
    }

    public void setFranchiseId(short franchiseId) {
        this.franchiseId = franchiseId;
    }

    public short getYearsWithFranchise() {
        return yearsWithFranchise;
    }

    public void setYearsWithFranchise(short yearsWithFranchise) {
        this.yearsWithFranchise = yearsWithFranchise;
    }

    public double getPerformanceRate() {
        return performanceRate;
    }

    public void setPerformanceRate(double performanceRate) {
        this.performanceRate = performanceRate;
    }
} // end class CoachPerformance
